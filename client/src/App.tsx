import { Switch, Route } from "wouter";
import { queryClient } from "./lib/queryClient";
import { QueryClientProvider } from "@tanstack/react-query";
import { Toaster } from "@/components/ui/toaster";
import { TooltipProvider } from "@/components/ui/tooltip";
import { SidebarProvider, SidebarTrigger } from "@/components/ui/sidebar";
import { AppSidebar } from "@/components/app-sidebar";
import { ThemeToggle } from "@/components/theme-toggle";
import { useAuth } from "@/hooks/use-auth";
import { Skeleton } from "@/components/ui/skeleton";

import AuthPage from "@/pages/auth-page";
import ForgotPasswordPage from "@/pages/forgot-password";
import LandingPage from "@/pages/landing";
import DashboardPage from "@/pages/dashboard";
import OverviewPage from "@/pages/overview";
import SlotConfigPage from "@/pages/slot-config";
import ServicesPage from "@/pages/services";
import HorairesPage from "@/pages/horaires";
import AgendaPage from "@/pages/agenda";
import WhatsAppPage from "@/pages/whatsapp";
import BlacklistPage from "@/pages/blacklist";
import SignalementsPage from "@/pages/signalements";
import AbonnementPage from "@/pages/abonnement";
import AdminPage from "@/pages/admin";
import NotFound from "@/pages/not-found";

function AuthenticatedRouter() {
  const sidebarStyle = {
    "--sidebar-width": "16rem",
    "--sidebar-width-icon": "3rem",
  };

  return (
    <SidebarProvider style={sidebarStyle as React.CSSProperties}>
      <div className="flex h-screen w-full">
        <AppSidebar />
        <div className="flex flex-col flex-1 min-w-0">
          <header className="flex items-center justify-between gap-4 p-3 border-b bg-background/95 backdrop-blur supports-[backdrop-filter]:bg-background/60 sticky top-0 z-40">
            <SidebarTrigger data-testid="button-sidebar-toggle" />
            <ThemeToggle />
          </header>
          <main className="flex-1 overflow-auto p-6">
            <Switch>
              <Route path="/" component={DashboardPage} />
              <Route path="/overview" component={OverviewPage} />
              <Route path="/slots/new" component={SlotConfigPage} />
              <Route path="/slots/:id" component={SlotConfigPage} />
              <Route path="/services" component={ServicesPage} />
              <Route path="/horaires" component={HorairesPage} />
              <Route path="/agenda" component={AgendaPage} />
              <Route path="/whatsapp" component={WhatsAppPage} />
              <Route path="/blacklist" component={BlacklistPage} />
              <Route path="/signalements" component={SignalementsPage} />
              <Route path="/abonnement" component={AbonnementPage} />
              <Route path="/admin" component={AdminPage} />
              <Route component={NotFound} />
            </Switch>
          </main>
        </div>
      </div>
    </SidebarProvider>
  );
}

